package com.btcdteam.nhom1_demo;

public class Class_cua_tuan {
    int id;
    int ten;

    public Class_cua_tuan(int id, int ten) {
        this.id = id;
        this.ten = ten;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getTen() {
        return ten;
    }

    public void setTen(int ten) {
        this.ten = ten;
    }
}
